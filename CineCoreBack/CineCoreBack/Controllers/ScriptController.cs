        [HttpPut("scene/{sceneId}/notes")]
        public async Task<IActionResult> UpdateNotes(int sceneId, [FromBody] string notes)
        {
            if (notes != null && notes.Length > 2000)
            {
                return BadRequest(new { message = "Notes cannot exceed 2000 characters." });
            }

            var scene = await _context.Scenes.FindAsync(sceneId);
            if (scene == null) return NotFound();

            scene.Notes = notes; // Зберігає всі \n та пробіли
            await _context.SaveChangesAsync();
            return NoContent();
        }

        [HttpPut("project/{projectId}/scenes/reorder")]
        public async Task<IActionResult> ReorderScenes(int projectId, [FromBody] List<int> orderedSceneIds)
        {
            if (orderedSceneIds == null || !orderedSceneIds.Any()) return Ok();

            // Викликаємо збережену процедуру для безпечного транзакційного сортування
            await _context.Database.ExecuteSqlRawAsync(
                "CALL sp_reorder_scenes({0}, {1})",
                projectId,
                orderedSceneIds.ToArray()
            );

            return Ok();
        }


