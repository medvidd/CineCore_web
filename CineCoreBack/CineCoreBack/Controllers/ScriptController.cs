        [HttpPost("project/{projectId}/role")]
        public async Task<IActionResult> QuickCreateRole(int projectId, [FromBody] QuickResourceDto dto)
        {
            if (string.IsNullOrWhiteSpace(dto.Name)) return BadRequest(new { message = "Role name cannot be empty." });

            var existingRole = await _context.Roles
                .FirstOrDefaultAsync(r => r.ProjectId == projectId && r.RoleName.ToLower() == dto.Name.ToLower());

            if (existingRole != null)
            {
                return Ok(new { id = existingRole.Id, name = existingRole.RoleName, color = existingRole.ColorHex });
            }

            var res = new Resource { ProjectId = projectId };
            _context.Resources.Add(res);
            await _context.SaveChangesAsync();

            var role = new Role { Id = res.Id, ProjectId = projectId, RoleName = dto.Name.Trim(), ColorHex = "#3AB9A0", IsAutoGenerated = false };
            _context.Roles.Add(role);
            await _context.SaveChangesAsync();

            return Ok(new { id = role.Id, name = role.RoleName, color = role.ColorHex });
        }

