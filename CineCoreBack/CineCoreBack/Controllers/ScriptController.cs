        [HttpPost("project/{projectId}/location")]
        public async Task<IActionResult> QuickCreateLocation(int projectId, [FromBody] QuickResourceDto dto)
        {
            if (string.IsNullOrWhiteSpace(dto.Name)) return BadRequest(new { message = "Location name cannot be empty." });

            var existingLoc = await _context.Locations
                .Include(l => l.IdNavigation)
                .FirstOrDefaultAsync(l => l.LocationName.ToLower() == dto.Name.ToLower());

            if (existingLoc != null)
            {
                if (existingLoc.IdNavigation.ProjectId == projectId)
                    return Ok(new { id = existingLoc.Id, name = existingLoc.LocationName, type = existingLoc.LocationType });
                else
                    return BadRequest(new { message = $"Location '{dto.Name}' already exists in another project. Please use a different name." });
            }

            var res = new Resource { ProjectId = projectId };
            _context.Resources.Add(res);
            await _context.SaveChangesAsync();

            var loc = new Location { Id = res.Id, LocationName = dto.Name.Trim(), LocationType = "interior" };
            _context.Locations.Add(loc);
            await _context.SaveChangesAsync();

            return Ok(new { id = loc.Id, name = loc.LocationName, type = loc.LocationType });
        }

