        // 1. АВТОМАТИЧНЕ ЗБЕРЕЖЕННЯ ТА СИНХРОНІЗАЦІЯ (З підтримкою Auto-Split)
        [HttpPost("scene/{sceneId}/autosave")]
        public async Task<IActionResult> AutoSaveScript(int sceneId, [FromBody] ScriptSaveDto dto)
        {
            var scene = await _context.Scenes.FindAsync(sceneId);
            if (scene == null) return NotFound();

            // ==========================================
            // КРОК А: Робота з персонажами (Існуючий функціонал)
            // ==========================================
            var existingRoles = await _context.Roles
                .Where(r => r.ProjectId == dto.ProjectId)
                .ToListAsync();

            var characterBlocks = dto.Blocks
                .Where(b => b.Type.ToLower() == "character")
                .Select(b => b.Content.Trim().ToUpper())
                .Distinct()
                .ToList();

            foreach (var name in characterBlocks)
            {
                var existing = existingRoles.FirstOrDefault(r => r.RoleName.ToUpper() == name);
                if (existing == null)
                {
                    var res = new Resource { ProjectId = dto.ProjectId };
                    _context.Resources.Add(res);
                    await _context.SaveChangesAsync();

                    var newRole = new Role
                    {
                        Id = res.Id,
                        ProjectId = dto.ProjectId,
                        RoleName = name,
                        IsAutoGenerated = true, // Новий персонаж завжди Auto
                        ColorHex = "#3AB9A0"
                    };
                    _context.Roles.Add(newRole);
                    existingRoles.Add(newRole);
                }
            }
            await _context.SaveChangesAsync();

            // ==========================================
            // КРОК Б: Логіка розділення сцен (Auto-Split)
            // ==========================================

            // 1. Рахуємо, скільки ДОДАТКОВИХ сцен нам треба створити
            int totalHeadings = dto.Blocks.Count(b => b.Type.ToLower() == "scene_heading");
            int extraScenesNeeded = totalHeadings > 1 ? totalHeadings - 1 : 0;

            // 2. Якщо є додаткові сцени, зсуваємо нумерацію (SequenceNum) всіх наступних сцен у проекті
            if (extraScenesNeeded > 0)
            {
                var trailingScenes = await _context.Scenes
                    .Where(s => s.ProjectId == dto.ProjectId && s.SequenceNum > scene.SequenceNum)
                    .ToListAsync();

                foreach (var s in trailingScenes)
                {
                    s.SequenceNum += extraScenesNeeded;
                }
                await _context.SaveChangesAsync();
            }

            // 3. Wipe & Replace (Очищення старих елементів ТІЛЬКИ для поточної сцени)
            var oldElements = _context.ScriptElements.Where(e => e.SceneId == sceneId);
            _context.ScriptElements.RemoveRange(oldElements);

            // ==========================================
            // КРОК В: Запис нових блоків і створення сцен
            // ==========================================
            Scene currentTargetScene = scene; // ОГОЛОШУЄМО ЗМІННУ ТУТ! Початково вказує на стару сцену.
            int order = 0;
            int? currentRoleId = null;
            bool isFirstHeading = true;
            int newScenesCreated = 0;

            // Словник для правильного розрахунку тривалості кожної сцени (старої і нових)
            var sceneBlocksMap = new Dictionary<Scene, List<ScriptBlockDto>>();
            sceneBlocksMap[currentTargetScene] = new List<ScriptBlockDto>();

            foreach (var block in dto.Blocks)
            {
                string type = block.Type.ToLower();

                if (type == "scene_heading")
                {
                    if (isFirstHeading)
                    {
                        currentTargetScene.SluglineText = block.Content;
                        sceneBlocksMap[currentTargetScene].Add(block); // Зберігаємо блок для розрахунку часу
                        isFirstHeading = false;
                    }
                    else
                    {
                        // ЗНАЙШЛИ НОВИЙ ЗАГОЛОВОК! Створюємо нову сцену
                        newScenesCreated++;
                        var newScene = new Scene
                        {
                            ProjectId = dto.ProjectId,
                            SequenceNum = scene.SequenceNum + newScenesCreated,
                            SluglineText = block.Content,
                            EstimatedDuration = TimeSpan.Zero
                        };
                        _context.Scenes.Add(newScene);
                        await _context.SaveChangesAsync(); // Зберігаємо, щоб отримати новий ID

                        currentTargetScene = newScene; // Перемикаємо "ціль" запису на нову сцену
                        sceneBlocksMap[currentTargetScene] = new List<ScriptBlockDto> { block };
                        order = 0; // Скидаємо порядок елементів для нової сцени
                        currentRoleId = null; // Скидаємо персонажа
                    }
                    continue; // Заголовок не зберігаємо як окремий ScriptElement у БД
                }

                // Записуємо елементи у поточну цільову сцену
                if (type == "character")
                {
                    currentRoleId = existingRoles.FirstOrDefault(r => r.RoleName.ToUpper() == block.Content.Trim().ToUpper())?.Id;
                }
                int? roleIdToSave = (type == "character" || type == "dialogue" || type == "parenthetical") ? currentRoleId : null;

                _context.ScriptElements.Add(new ScriptElement
                {
                    SceneId = currentTargetScene.Id, // Використовуємо ID поточної цілі
                    OrderIndex = order++,
                    Content = block.Content,
                    ElementType = type,
                    RoleId = roleIdToSave
                });

                sceneBlocksMap[currentTargetScene].Add(block); // Зберігаємо блок для розрахунку часу
            }

            await _context.SaveChangesAsync();


            // ==========================================
            // КРОК Д: Розрахунок тривалості сцен
            // ==========================================
            foreach (var kvp in sceneBlocksMap)
            {
                // Перераховуємо час тільки для тих блоків, які належать конкретній сцені
                kvp.Key.EstimatedDuration = EstimateSceneDuration(kvp.Value);
            }
            await _context.SaveChangesAsync();

            var duration = scene.EstimatedDuration.HasValue
                ? $"{(int)scene.EstimatedDuration.Value.TotalMinutes:D2}:{scene.EstimatedDuration.Value.Seconds:D2}"
                : null;

            return Ok(new { message = "Synced", estimatedDuration = duration, wasSplit = extraScenesNeeded > 0 });
        }

        // ОТРИМАННЯ СКРИПТУ ОДНІЄЇ СЦЕНИ (Відновлений метод)
        [HttpGet("scene/{sceneId}")]
        public async Task<IActionResult> GetScript(int sceneId)
        {
            var scene = await _context.Scenes
                .Include(s => s.ScriptElements).ThenInclude(e => e.Role)
                .FirstOrDefaultAsync(s => s.Id == sceneId);

            if (scene == null) return NotFound();

            var blocks = new List<object> {
                new { id = "h-" + scene.Id, type = "scene_heading", content = scene.SluglineText, sceneCode = "SC-" + scene.SequenceNum.ToString().PadLeft(3, '0') }
            };

            var elements = scene.ScriptElements.OrderBy(e => e.OrderIndex).Select(e => new {
                id = e.Id.ToString(),
                type = e.ElementType,
                content = e.Content,
                charId = e.RoleId?.ToString(),
                color = (e.ElementType == "character" || e.ElementType == "dialogue" || e.ElementType == "parenthetical") ? (e.Role?.ColorHex ?? "#444") : "#444"
            });
            blocks.AddRange(elements);

            // ДОДАНО: Витягуємо прикріплені ресурси сцени
            var sceneResources = await _context.SceneResources
                .Include(sr => sr.Resource.Role).Include(sr => sr.Resource.Location).Include(sr => sr.Resource.Prop)
                .Where(sr => sr.SceneId == sceneId).ToListAsync();

            var linkedRoles = sceneResources.Where(sr => sr.Resource.Role != null).Select(sr => new { id = sr.ResourceId, name = sr.Resource.Role.RoleName, color = sr.Resource.Role.ColorHex }).ToList();
            var linkedLocations = sceneResources.Where(sr => sr.Resource.Location != null).Select(sr => new { id = sr.ResourceId, name = sr.Resource.Location.LocationName, type = sr.Resource.Location.LocationType }).ToList();
            var linkedProps = sceneResources.Where(sr => sr.Resource.Prop != null).Select(sr => new { id = sr.ResourceId, name = sr.Resource.Prop.PropName }).ToList();

            return Ok(new { notes = scene.Notes, blocks = blocks, linkedRoles, linkedLocations, linkedProps });
        }

        // 1. ОТРИМАННЯ СПИСКУ СЦЕН (ДЛЯ ЛІВОЇ ПАНЕЛІ)
        [HttpGet("project/{projectId}/scenes")]
        public async Task<IActionResult> GetProjectScenes(int projectId)
        {
            var scenes = await _context.Scenes
                .Where(s => s.ProjectId == projectId)
                .OrderBy(s => s.SequenceNum)
                .Select(s => new {
                    id = s.Id,
                    sequenceNum = s.SequenceNum,
                    sluglineText = s.SluglineText,
                    estimatedDuration = s.EstimatedDuration
                })
                .ToListAsync();

            var result = scenes.Select(s => new {
                s.id,
                s.sequenceNum,
                s.sluglineText,
                // Формат "MM:SS" для UI; null якщо ще не розраховано
                estimatedDuration = s.estimatedDuration.HasValue
                    ? $"{(int)s.estimatedDuration.Value.TotalMinutes:D2}:{s.estimatedDuration.Value.Seconds:D2}"
                    : (string?)null
            });

            return Ok(result);
        }

        // ПЕРЕРАХУНОК ТРИВАЛОСТІ ВСІХ СЦЕН ПРОЕКТУ (одноразовий або при потребі)
        [HttpPost("project/{projectId}/recalculate-durations")]
        public async Task<IActionResult> RecalculateAllDurations(int projectId)
        {
            var scenes = await _context.Scenes
                .Include(s => s.ScriptElements)
                .Where(s => s.ProjectId == projectId)
                .ToListAsync();

            foreach (var scene in scenes)
            {
                // Конвертуємо ScriptElement назад у ScriptBlockDto для переліку
                // scene_heading додаємо окремо з SluglineText
                var blocks = new List<ScriptBlockDto>
                {
                    new ScriptBlockDto { Type = "scene_heading", Content = scene.SluglineText }
                };

                blocks.AddRange(scene.ScriptElements
                    .OrderBy(e => e.OrderIndex)
                    .Select(e => new ScriptBlockDto
                    {
                        Type = e.ElementType,
                        Content = e.Content
                    }));

                scene.EstimatedDuration = EstimateSceneDuration(blocks);
            }

            await _context.SaveChangesAsync();

            return Ok(new { updated = scenes.Count });
        }

        [HttpGet("project/{projectId}/full")]
        public async Task<IActionResult> GetFullScript(int projectId)
        {
            var scenes = await _context.Scenes
                .Include(s => s.ScriptElements).ThenInclude(e => e.Role)
                .Where(s => s.ProjectId == projectId)
                .OrderBy(s => s.SequenceNum)
                .ToListAsync();

            var result = new List<object>();

            foreach (var scene in scenes)
            {
                // Додаємо заголовок як блок
                result.Add(new
                {
                    id = "h-" + scene.Id,
                    type = "scene_heading",
                    content = scene.SluglineText,
                    sceneId = scene.Id,
                    sceneCode = "SC-" + scene.SequenceNum.ToString().PadLeft(3, '0')
                });

                // Додаємо елементи
                var elements = scene.ScriptElements.OrderBy(e => e.OrderIndex).Select(e => new {
                    id = e.Id.ToString(),
                    type = e.ElementType,
                    content = e.Content,
                    charId = e.RoleId,
                    color = (e.ElementType == "character" || e.ElementType == "dialogue" || e.ElementType == "parenthetical")
                        ? (e.Role?.ColorHex ?? "#444")
                        : "#444",
                    sceneId = scene.Id
                });
                result.AddRange(elements);
            }

            // Збираємо всі прикріплені ресурси для проекту (Для правої панелі у Full Script)
            var sceneResources = await _context.SceneResources
                .Include(sr => sr.Resource.Location)
                .Include(sr => sr.Resource.Prop)
                .Where(sr => sr.Scene.ProjectId == projectId)
                .ToListAsync();

            var linkedLocations = sceneResources
                .Where(sr => sr.Resource.Location != null)
                .Select(sr => new { id = sr.ResourceId, name = sr.Resource.Location.LocationName, type = sr.Resource.Location.LocationType })
                .DistinctBy(l => l.id) // Беремо тільки унікальні, щоб не дублювалися
                .ToList();

            var linkedProps = sceneResources
                .Where(sr => sr.Resource.Prop != null)
                .Select(sr => new { id = sr.ResourceId, name = sr.Resource.Prop.PropName })
                .DistinctBy(p => p.id) // Беремо тільки унікальні
                .ToList();

            return Ok(new
            {
                blocks = result,
                linkedLocations = linkedLocations,
                linkedProps = linkedProps
            });
        }

